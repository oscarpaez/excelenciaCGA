package co.com.CGAwebComercial.util;

import java.util.List;

public class Fechas {
	
	private String valorMes;
	
	private String mes;
	
	private List<Fechas> listaFechas;
	
	
	public String getValorMes() {
		return valorMes;
	}

	public void setValorMes(String valorMes) {
		this.valorMes = valorMes;
	}

	public String getMes() {
		return mes;
	}

	public void setMes(String mes) {
		this.mes = mes;
	}

	public List<Fechas> getListaFechas() {
		return listaFechas;
	}

	public void setListaFechas(List<Fechas> listaFechas) {
		this.listaFechas = listaFechas;
	}
	
	

}
